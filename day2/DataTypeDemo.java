package com.tnsif.day2;

public class DataTypeDemo {
	public static void main(String[] args)
	{
		int value1 = 10/3;
		float value2 = 10f/3f;
		double value3 = 10d/3d;
		System.out.println("value1: " +value1);
		System.out.println("value2: " +value2);
		System.out.println("value3: " +value3);
	}

}
